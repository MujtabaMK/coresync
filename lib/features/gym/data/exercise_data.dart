        .where((e) => e.difficulty == difficulty)
        .toList();
  }

  static ExerciseModel? getById(String id) {
    for (final list in _exercises.values) {
      for (final exercise in list) {
        if (exercise.id == id) return exercise;
      }
    }
    return null;
  }
