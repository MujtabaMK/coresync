  static List<ExerciseModel> getByCategory(String category) {
    return _exercises[category] ?? [];
  }

  static List<ExerciseModel> getByDifficulty(ExerciseDifficulty difficulty) {
    return _exercises.values
        .expand((list) => list)
        .where((e) => e.difficulty == difficulty)
        .toList();
  }

  static List<ExerciseModel> getByCategoryAndDifficulty(
    String category,
    ExerciseDifficulty difficulty,
  ) {
    return getByCategory(category)
        .where((e) => e.difficulty == difficulty)
        .toList();
  }

  static ExerciseModel? getById(String id) {
    for (final list in _exercises.values) {
      for (final exercise in list) {
        if (exercise.id == id) return exercise;
      }
    }
    return null;
  }
